package com.kelani.demo.DAO;

public class ResultByPartyDAO {


    private String id;

    private String name;

    private int noOfVote;

    public ResultByPartyDAO() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNoOfVote() {
        return noOfVote;
    }

    public void setNoOfVote(int noOfVote) {
        this.noOfVote = noOfVote;
    }
}
